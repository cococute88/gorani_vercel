def _format_calendar_value(value: Any) -> str:
    if value is None:
        return "-"
    text = str(value).strip()
    return text if text else "-"


def _parse_economic_event_date(date_value: Any) -> Optional[pd.Timestamp]:
    raw = "" if date_value is None else str(date_value).strip()
    if not raw:
        return None

    normalized = raw.replace(".", "/").replace("-", "/")
    current_year = pd.Timestamp.now(tz="Asia/Seoul").year
    date_candidates = [raw]
    if re.fullmatch(r"\d{1,2}/\d{1,2}", normalized):
        date_candidates.insert(0, f"{current_year}/{normalized}")

    for candidate in date_candidates:
        try:
            return pd.Timestamp(candidate)
        except Exception:
            continue
    return None


def _format_economic_event_date(date_value: Any) -> str:
    raw = "" if date_value is None else str(date_value).strip()
    if not raw:
        return "--/--"

    parsed = _parse_economic_event_date(raw)
    if parsed is not None:
        weekdays = ["월", "화", "수", "목", "금", "토", "일"]
        return f"{parsed.month}/{parsed.day}({weekdays[parsed.weekday()]})"

    if len(raw) >= 10 and re.fullmatch(r"\d{4}-\d{1,2}-\d{1,2}.*", raw):
        return raw[5:10].replace("-", "/")
    return raw


def _sort_key_economic_time(time_value: Any) -> str:
    text = _format_calendar_value(time_value)
    if text == "-":
        return "99:99"
    match = re.search(r"(\d{1,2})[:：](\d{2})", text)
    if match:
        return f"{int(match.group(1)):02d}:{match.group(2)}"
    return text


def _parse_economic_updated_at(updated_at: Any) -> Optional[pd.Timestamp]:
    raw = "" if updated_at is None else str(updated_at).strip()
    if not raw:
        return None
    try:
        ts = pd.Timestamp(raw)
        if ts.tzinfo is None:
            return ts.tz_localize("Asia/Seoul")
        return ts.tz_convert("Asia/Seoul")
    except Exception:
        return None


def _format_economic_updated_at(updated_at: Any) -> Optional[str]:
    ts = _parse_economic_updated_at(updated_at)
    if ts is not None:
        return ts.strftime("%Y-%m-%d %H:%M")

    raw = "" if updated_at is None else str(updated_at).strip()
    return raw[:16].replace("T", " ") if raw else None


def _is_economic_data_stale(updated_at: Any) -> bool:
    ts = _parse_economic_updated_at(updated_at)
    if ts is None:
        return False
    age = pd.Timestamp.now(tz="Asia/Seoul") - ts
    return age >= pd.Timedelta(hours=ECONOMIC_CALENDAR_STALE_HOURS)


def _normalize_economic_calendar_payload(payload: Any) -> Dict[str, Any]:
    """Normalize legacy list and new metadata-object calendar payloads."""
    if isinstance(payload, list):
        return {
            "payload_type": "list",
            "status": "ok",
            "updated_at": None,
            "source": "legacy-list",
            "error": None,
            "events_payload": payload,
        }
    if isinstance(payload, dict):
        events_payload = payload.get("events", [])
        return {
            "payload_type": "dict",
            "status": str(payload.get("status") or "ok"),
            "updated_at": payload.get("updated_at"),
            "source": payload.get("source"),
            "error": payload.get("error"),
            "events_payload": events_payload if isinstance(events_payload, list) else [],
        }
    return {
        "payload_type": type(payload).__name__,
        "status": "invalid_file",
        "updated_at": None,
        "source": None,
        "error": "payload must be a list or dict",
        "events_payload": [],
    }


def _build_economic_debug_info(
    *,
    payload_type: str,
    status: str,
    updated_at: Any,
    event_count: int,
    is_stale: bool,
    error: Any = None,
) -> Dict[str, Any]:
    return {
        "status": status,
        "updated_at": updated_at,
        "events_count": event_count,
        "is_stale": is_stale,
        "json_path": str(ECONOMIC_CALENDAR_JSON_PATH),
        "error": error,
        "payload_type": payload_type,
    }


@st.cache_data(ttl=60 * 5, show_spinner=False)
def load_us_high_importance_economic_calendar() -> Tuple[List[Dict[str, Any]], Optional[str], Optional[str], bool, Dict[str, Any]]:
    """Load pre-generated U.S. high-importance economic calendar JSON without network calls.

    Supports both the legacy list payload and the new metadata object payload.
    """
    if not ECONOMIC_CALENDAR_JSON_PATH.exists():
        debug_info = _build_economic_debug_info(
            payload_type="missing",
            status="not_initialized",
            updated_at=None,
            event_count=0,
            is_stale=False,
        )
        return [], "not_initialized", None, False, debug_info

    try:
        with ECONOMIC_CALENDAR_JSON_PATH.open("r", encoding="utf-8") as f:
            payload = json.load(f)
    except Exception as exc:
        debug_info = _build_economic_debug_info(
            payload_type="invalid",
            status="invalid_file",
            updated_at=None,
            event_count=0,
            is_stale=False,
            error=str(exc),
        )
        return [], "invalid_file", None, False, debug_info

    normalized = _normalize_economic_calendar_payload(payload)
    payload_type = normalized["payload_type"]
    status = normalized["status"]
    raw_updated_at = normalized["updated_at"]
    events_payload = normalized["events_payload"]
    event_count = len(events_payload)
    is_stale = _is_economic_data_stale(raw_updated_at)
    updated_at = _format_economic_updated_at(raw_updated_at)

    events: List[Dict[str, Any]] = []
    latest_event_updated_at: Optional[str] = None
    for original_index, item in enumerate(events_payload):
        if not isinstance(item, dict):
            continue
        raw_date = "" if item.get("date") is None else str(item.get("date")).strip()
        try:
            event_date = pd.Timestamp(raw_date).date()
            today_kst = pd.Timestamp.now(tz="Asia/Seoul").date()
            if event_date < today_kst or event_date > today_kst + timedelta(days=ECONOMIC_CALENDAR_DISPLAY_DAYS):
                continue
        except Exception:
            pass
        parsed_sort_date = _parse_economic_event_date(raw_date)
        sort_date = parsed_sort_date.date().isoformat() if parsed_sort_date is not None else raw_date
        event = {
            "date": _format_economic_event_date(item.get("date")),
            "time": _format_calendar_value(item.get("time")),
            "name": _format_calendar_value(item.get("name") or item.get("raw_name")),
            "updated_at": item.get("updated_at"),
            "_sort_date": sort_date,
            "_original_index": original_index,
        }
        events.append(event)
        if item.get("updated_at"):
            latest_event_updated_at = str(item.get("updated_at"))

    if not raw_updated_at and latest_event_updated_at:
        updated_at = _format_economic_updated_at(latest_event_updated_at)
        is_stale = _is_economic_data_stale(latest_event_updated_at)

    events.sort(
        key=lambda event: (
            str(event.get("_sort_date") or ""),
            _sort_key_economic_time(event.get("time")),
            int(event.get("_original_index", 0)),
        )
    )

    debug_info = _build_economic_debug_info(
        payload_type=payload_type,
        status=status,
        updated_at=raw_updated_at or latest_event_updated_at,
        event_count=event_count,
        is_stale=is_stale,
        error=normalized.get("error"),
    )
    return events, None, updated_at, is_stale, debug_info



