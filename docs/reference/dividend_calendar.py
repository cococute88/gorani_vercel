def _migrate_custom_symbols(uid: str) -> None:
    """기존의 'ⓕ'와 'ⓒ' 심볼을 새로운 '⚠'와 '※' 심볼로 마이그레이션합니다."""
    ce_data = load_custom_events(uid)
    changed = False
    for date_str, c_data in ce_data.items():
        sym = c_data.get("symbol")
        if sym == "ⓕ":
            c_data["symbol"] = "⚠"
            changed = True
        elif sym == "ⓒ":
            c_data["symbol"] = "※"
            changed = True
    if changed:
        save_custom_events(uid, ce_data)


