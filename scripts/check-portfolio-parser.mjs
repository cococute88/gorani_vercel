function assertQuoteEligibility() {
  const cashKeywordGuesses = ["cash reserve", "deposit account", "pension fund", "annuity insurance"];
  for (const productName of cashKeywordGuesses) {
    const guess = guessTicker(productName);
    assert.equal(guess.ticker, null, `${productName} should not get a guessed pseudo ticker`);
    assert.equal(guess.matchedBy, "cash", `${productName} should still be recognized as cash-like`);
  }

  const eligible = ["QQQ", "SCHD", "TQQQ", "QLD", "SPY", "VOO"];
  for (const ticker of eligible) {
    const holding = makeHolding(ticker);
    assert.equal(getQuoteTickerForHolding(holding), ticker);
    assert.equal(isQuoteEligibleHolding(holding), true);
    assert.equal(canRevalueHoldingWithQuote(holding), true);
  }

  const koreanEtf = makeHolding("360200.KS", {
    productName: "ACE미국S&P500",
    assetType: "ETF",
  });
  assert.equal(getQuoteTickerForHolding(koreanEtf), "360200.KS");
  assert.equal(isQuoteEligibleHolding(koreanEtf), true);
  assert.equal(canRevalueHoldingWithQuote(koreanEtf), true);

  const nonQuoteHoldings = [
    makeHolding("CASH_LIKE", { productName: "cash reserve" }),
    makeHolding(undefined, { productName: "deposit account" }),
    makeHolding(undefined, { productName: "saving account" }),
    makeHolding(undefined, { productName: "pension fund" }),
    makeHolding(undefined, { productName: "annuity insurance" }),
    makeHolding(undefined, { productName: "real estate" }),
    makeHolding("005930.KS", { productName: "Samsung Electronics", assetType: "국내주식" }),
    makeHolding("BTC", { productName: "Bitcoin" }),
  ];

  for (const holding of nonQuoteHoldings) {
    assert.equal(isQuoteEligibleHolding(holding), false, `${holding.productName} should not be quote eligible`);
    assert.equal(getQuoteTickerForHolding(holding), null, `${holding.productName} should not produce a quote ticker`);
    assert.equal(canRevalueHoldingWithQuote(holding), false, `${holding.productName} should not be revaluable`);
  }

  assert.equal(canRevalueHoldingWithQuote(makeHolding("QQQ", { quantity: undefined })), false);
  assert.equal(canRevalueHoldingWithQuote(makeHolding("QQQ", { quantity: 0 })), false);
  assert.equal(canRevalueHoldingWithQuote(makeHolding("QQQ", { quantity: -1 })), false);
  assert.equal(canRevalueHoldingWithQuote(makeHolding("QQQ", { quantity: 0.25 })), true);

  const mixed = [
    makeHolding("SCHD", { quantity: 2 }),
    makeHolding("QQQ", { quantity: undefined }),
    makeHolding("CASH_LIKE", { productName: "cash reserve" }),
    makeHolding("VOO", { quantity: 0 }),
    makeHolding("SCHD", { quantity: 5 }),
  ];
  assert.deepEqual(
    extractQuoteEligibleHoldings(mixed).map((holding) => holding.ticker),
    ["SCHD", "QQQ", "VOO", "SCHD"],
  );
  assert.deepEqual(getUniqueQuoteTickers(mixed), ["QQQ", "SCHD", "VOO"]);

  return {
    eligibleTickers: eligible.join(", "),
    nonQuoteRows: nonQuoteHoldings.length,
    uniqueTickers: getUniqueQuoteTickers(mixed).join(", "),
  };
}

function text(value) {
  return String(value ?? "").trim();
}

function compact(value) {
  return text(value).replace(/\s+/g, "");
}

function worksheetRows(worksheet) {
  return XLSX.utils.sheet_to_json(worksheet, {
    header: 1,
    defval: null,
    blankrows: true,
    raw: true,
  });
}

function workbookStructureSummary(workbook) {
  return workbook.SheetNames.map((sheetName) => {
    const worksheet = workbook.Sheets[sheetName];
    const rows = worksheetRows(worksheet);
    const range = worksheet["!ref"] ? XLSX.utils.decode_range(worksheet["!ref"]) : null;
    const sectionRows = [];
    const headerRows = [];

    for (let rowIndex = 0; rowIndex < rows.length; rowIndex += 1) {
      const row = rows[rowIndex] ?? [];
      const normalizedCells = row.map(compact);
      const displayCells = row.map(text);
      const rowNumber = range ? range.s.r + rowIndex + 1 : rowIndex + 1;

      for (const cell of normalizedCells) {
        if (/^\d+\.[^\d.]/.test(cell)) {
          sectionRows.push({ rowNumber, label: cell });
        }
      }

      const hasProduct = normalizedCells.includes(K.product);
      const hasAmount = normalizedCells.some((cell) => cell.includes(K.amount));
      const hasPrincipal = normalizedCells.some((cell) => cell.includes(K.principal));
      if (hasProduct && (hasAmount || hasPrincipal)) {
        headerRows.push({
          rowNumber,
          headers: displayCells
            .map((value, index) => ({
              column: range ? XLSX.utils.encode_col(range.s.c + index) : XLSX.utils.encode_col(index),
              value,
            }))
            .filter((cell) => cell.value),
        });
      }
    }

    return {
      sheetName,
      ref: worksheet["!ref"] ?? "",
      rowCount: rows.length,
      nonEmptyRows: rows.filter((row) => row.some((cell) => text(cell) !== "")).length,
      mergeCount: (worksheet["!merges"] ?? []).length,
      sectionRows,
      headerRows,
    };
  });
}

function summarizeParseResult(result) {
  const optionalFieldCounts = {
    quantity: result.holdings.filter((holding) => holding.quantity !== undefined).length,
    currency: result.holdings.filter((holding) => holding.currency !== undefined).length,
    ticker: result.holdings.filter((holding) => holding.ticker !== undefined).length,
    currentPrice: result.holdings.filter((holding) => holding.currentPrice !== undefined).length,
    averagePrice: result.holdings.filter((holding) => holding.averagePrice !== undefined).length,
    accountName: result.holdings.filter((holding) => holding.accountName !== undefined).length,
    valueOriginalCurrency: result.holdings.filter((holding) => holding.valueOriginalCurrency !== undefined).length,
  };

  return {
    ok: result.ok,
    sheetName: result.sheetName,
    snapshotDate: result.snapshotDate,
    errors: result.errors.length,
    warnings: result.warnings.length,
    holdings: result.holdings.length,
    financeAssets: result.financeAssets.length,
    investmentValueRows: result.holdings.filter((holding) => holding.valueKRW > 0).length,
    excludedSmallCount: result.excludedSmallCount,
    excludedBelowMinimumCount: result.excludedBelowMinimumCount,
    optionalFieldCounts,
    quoteEligible: extractQuoteEligibleHoldings(result.holdings).length,
    uniqueQuoteTickers: getUniqueQuoteTickers(result.holdings).length,
    canRevalue: result.holdings.filter((holding) => canRevalueHoldingWithQuote(holding)).length,
  };
}

function parsePrivatePathFromArgs(argv) {
  const index = argv.indexOf("--private");
  if (index < 0) return null;
  const next = argv[index + 1];
  if (!next || next.startsWith("--")) {
    return path.join(
      rootDir,
      "docs",
      "fixtures",
      "portfolio-parser",
      "private",
      "2025-06-12~2026-06-12.xlsx",
    );
  }
  return path.resolve(rootDir, next);
}

function checkPrivateSample(filePath) {
  if (!fs.existsSync(filePath)) {
    console.log(`Private sample not found, skipped: ${path.relative(rootDir, filePath)}`);
    return null;
  }

  const workbook = XLSX.readFile(filePath, { cellDates: true });
  const result = parseBanksaladWorkbook(workbook, path.basename(filePath));
  assert.equal(result.ok, true, `private sample should parse without errors: ${result.errors.join("; ")}`);
  assert.equal(result.holdings.length > 0, true, "private sample should produce holdings");
  assert.equal(result.financeAssets.length > 0, true, "private sample should produce finance assets");

  return {
    file: path.relative(rootDir, filePath),
    workbook: workbookStructureSummary(workbook),
    parser: summarizeParseResult(result),
  };
}

function main() {
  const parserRows = aliasCases.map(assertParserCase);
  const quoteSummary = assertQuoteEligibility();
  const privatePath = parsePrivatePathFromArgs(process.argv.slice(2));

  console.log("Portfolio parser fixture regression passed.");
  console.table(parserRows);
  console.table([quoteSummary]);

  if (privatePath) {
    const privateSummary = checkPrivateSample(privatePath);
    if (privateSummary) {
      console.log("Private Banksalad sample parser check passed.");
      console.log(JSON.stringify(privateSummary, null, 2));
    }
  }
}

try {
  main();
} catch (error) {
  console.error("Portfolio parser fixture regression failed.");
  console.error(error);
  process.exit(1);
}
