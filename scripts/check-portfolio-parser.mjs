function byTicker(result, ticker) {
  return result.holdings.find((holding) => holding.ticker === ticker);
}

function assertParserCase(aliasCase) {
  const result = parseBanksaladWorkbook(
    makeWorkbook(aliasCase),
    `banksalad-parser-${aliasCase.name}-2026-06-12.xlsx`,
  );

  assert.equal(result.ok, true, `${aliasCase.name}: parser should succeed: ${result.errors.join("; ")}`);
  assert.equal(result.snapshotDate, "2026-06-12", `${aliasCase.name}: snapshot date should come from file name`);
  assert.equal(result.holdings.length, 4, `${aliasCase.name}: aggregate and small rows should be excluded`);
  assert.equal(result.financeAssets.some((asset) => asset.productName === "Checking account"), true);
  assert.equal(result.financeAssets.some((asset) => asset.productName === "Tiny wallet"), false);
  assert.equal(result.excludedBelowMinimumCount >= 1, true, `${aliasCase.name}: below-minimum exclusion should be counted`);
  assert.equal(result.excludedSmallCount >= 1, true, `${aliasCase.name}: explicit small exclusion should be counted`);
  assert.equal(result.holdings.some((holding) => holding.productName === "total"), false);
  assert.equal(result.holdings.some((holding) => holding.productName === "Should stay excluded after total"), false);

  const qqq = byTicker(result, "QQQ");
  assert.ok(qqq, `${aliasCase.name}: QQQ holding should be parsed`);
  assert.equal(qqq.productName, "Invesco QQQ Trust");
  assert.equal(qqq.broker, "Broker A");
  assert.equal(qqq.accountName, "Taxable");
  assert.equal(qqq.assetType, "ETF");
  assert.equal(qqq.principalKRW, 1_000_000);
  assert.equal(qqq.valueKRW, 1_200_000);
  assert.equal(qqq.quantity, 3);
  assert.equal(qqq.averagePrice, 330.5);
  assert.equal(qqq.currency, "USD");
  assert.equal(qqq.currentPrice, 400);
  assert.equal(qqq.valueOriginalCurrency, 1_200);

  const voo = byTicker(result, "VOO");
  assert.ok(voo, `${aliasCase.name}: VOO holding should be parsed`);
  assert.equal(voo.quantity, undefined);
  assert.equal(voo.currentPrice, undefined);
  assert.equal(voo.valueOriginalCurrency, undefined);

  const cash = byTicker(result, "USD");
  assert.ok(cash, `${aliasCase.name}: explicit non-quote ticker value should stay available`);
  assert.equal(isQuoteEligibleHolding(cash), false);

  return {
    aliasCase: aliasCase.name,
    holdings: result.holdings.length,
    financeAssets: result.financeAssets.length,
    excludedSmall: result.excludedSmallCount,
    excludedBelowMinimum: result.excludedBelowMinimumCount,
    qqqValueOriginalCurrency: qqq.valueOriginalCurrency,
    qqqAveragePrice: qqq.averagePrice,
  };
}

